#!/usr/bin/env bash
git config --global alias.co checkout
git config --global alias.br branch
git config --global alias.ci commit
git config --global alias.st status
git config --global alias.ft fetch
git config --global alias.ftu 'fetch upstream'
git config --global alias.unstage 'reset HEAD --'
git config --global alias.last 'log -1 HEAD'
git config --global alias.rb rebase
git config --global alias.rb-cont 'rebase --continue'
git config --global alias.rb-ab 'rebase --abort'
git config --global alias.chr cherry-pick
git config --global alias.chr-cont 'cherry-pick --continue'
git config --global alias.chr-ab 'cherry-pick --abort'



